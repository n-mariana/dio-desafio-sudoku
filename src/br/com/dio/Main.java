package br.com.dio;

import br.com.dio.model.Board;
import br.com.dio.model.Space;
import java.util.*;
import static java.util.Objects.nonNull;
import static java.util.Objects.isNull;
import java.util.stream.*;

public class Main {

    private final static Scanner scanner = new Scanner(System.in);
    private static Board board;
    private final static int BOARD_LIMIT = 9;

    public static void main(String[] args){
        final var positions = Stream.of(args)
            .collect(Collectors.toMap(
                k -> k.split(";")[0],
                v -> v.split(";")[1]
            ));
            var option = -1;
            while (true) {
                System.out.println("Selecione uma das opções a seguir:");
                System.out.println("1 - Iniciar um novo jogo");
                System.out.println("2 - Adicionar um novo número");
                System.out.println("3 - Remover um número");
                System.out.println("4 - Visualizar o jogo atual");
                System.out.println("5 - Verificar o status do jogo");
                System.out.println("6 - Reiniciar o jogo");
                System.out.println("7 - Finalizar o jogo");
                System.out.println("8 - Sair");

                option = scanner.nextInt();

                switch (option) {
                    case 1 -> startGame(positions);
                    case 2 -> inputNumber();
                    case 3 -> removeNumber();
                    case 4 -> showCurrentGame();
                    case 5 -> showGameStatus();
                    case 6 -> clearGame();
                    case 7 -> finishGame();
                    case 8 -> System.exit(0);
                    default -> System.out.println("Opção inválida, selecione uma das opções do menu");
                } 
            }    
        }

        private static void startGame(final Map<String, String> positions){
            if(nonNull(board)){
                System.out.println("O jogo foi iniciado");
                return;
            }
            List<List<Space>> spaces = new ArrayList<>();
            for(int i = 0; i < BOARD_LIMIT; i++){
                spaces.add(new ArrayList<>());
            for(int j = 0; j < BOARD_LIMIT; j++){
                var positionConfig = positions.get("%s, %s".formatted(i, j));
                var expected = Integer.parseInt(positionConfig.split(";")[0]);
                var fixed = Boolean.parseBoolean(positionConfig.split(";")[1]);
                var currentSpace = new Space(expected, fixed);
                spaces.get(i).add(currentSpace);
            }
        }
        board = new Board(spaces);
        System.out.println("O jogo está pronto para iniciar");
    }


        private static void inputNumber(){
            if (isNull(board)) {
                System.out.println("O jogo ainda não foi iniciado");
                return;
            }
            System.out.println("Informe a coluna em que o número será inserido ");
            var col = runUntilGetValidNumber(0, 8);
            System.out.println("Informe a linha em que o número será inserido ");
            var row = runUntilGetValidNumber(0, 8);
            System.out.printf("Informe o número que vai entrar na posição [%s,%s]\n", col, row);
            var value = runUntilGetValidNumber(1, 9);
            if(!board.changeValue(col, row, value)){
                System.out.printf("A posição [%s,%s] tem um valor fixo\n", col, row);
            }

        }

        private static void removeNumber(){
            if (isNull(board)) {
                System.out.println("O jogo ainda não foi iniciado");
                return;
            }
            System.out.println("Informe a coluna em que o número será inserido ");
            var col = runUntilGetValidNumber(0, 8);
            System.out.println("Informe a linha em que o número será inserido ");
            var row = runUntilGetValidNumber(0, 8);
            System.out.printf("Informe o número que vai entrar na posição [%s,%s]\n", col, row);
            if(!board.clearValue(col, row, null)){
                System.out.printf("A posição [%s,%s] tem um valor fixo\n", col, row);
            }

        }

        private static void showCurrentGame(){

        }

        private static void showGameStatus(){

        }

        private static void clearGame(){

        }

        private static void finishGame(){

        }

        private static int runUntilGetValidNumber(final int min, final int max){
            var current = scanner.nextInt();
            while (current < min || current > max) {
                System.out.printf("Informe um número entre %s e %s\n", min, max);
                current = scanner.nextInt();
            }
            return current;
        }
}
